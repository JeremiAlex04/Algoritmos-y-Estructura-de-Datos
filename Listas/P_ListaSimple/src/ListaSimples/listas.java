    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        String cod = txt_id.getText();

        if (cod.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Ingrese un codigo porfavor");
        } else {

            //Llamada a la funcion que retorna la posicion del dato buscado
            pFound = Buscar(ini, cod);

            //Verificando el puntero pFound para mostrar la inf. buscada
            if (pFound != null) {
                txt_id.setText(pFound.id);
                txt_nombre.setText(pFound.nombre);
                txt_exp.setText(pFound.añosExperiencia);
                txt_Vehiculo.setText(pFound.vehiculo);
                
                if (pFound.calificacion.equals("Buena")) {
                    jComboBox_cal.setSelectedIndex(0);
                } else {
                    jComboBox_cal.setSelectedIndex(1);
                }
   
                //Habilitar los objetos para eliminar y actualizar
                Habilitar();

            } else {
                JOptionPane.showMessageDialog(this, "El codigo: " + cod + ", no esta en la lista...");
            }

        }


    }//GEN-LAST:event_btnConsultarActionPerformed

