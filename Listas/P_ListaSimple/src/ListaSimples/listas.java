    void VerDatos() {
        //Ahora - conductor UBER
        String cod = null, nom, exp, veh, cal;
        Nodo aux = ini;
        num = 0;

        //Colocando el Encabezado
        Encabezado();

        //Recorriendo la Lista
        while (aux != null) {

            //Ahora - Conductor UBER
            cod = aux.id;
            nom = aux.nombre;
            exp = aux.añosExperiencia;
            veh = aux.vehiculo;
            cal = aux.calificacion;

            num++;
            String numera = String.valueOf(num);

            //Nodificando el tamaño de la numeracion con espacios en blanco a la izquierda
            for (int i = String.valueOf(num).length(); i < 5; i++) {
                numera = " " + numera;
            }
            //Ahora - Conductor de UBER

            // ID
            for (int i = cod.length(); i < 12; i++) {
                cod = cod + " ";
            }

            // Nombre
            for (int i = nom.length(); i < 20; i++) {
                nom = nom + " ";
            }

            // Años de experiencia
            for (int i = exp.length(); i < 8; i++) {
                exp = " " + exp;
            }

            // Vehículo
            for (int i = veh.length(); i < 15; i++) {
                veh = veh + " ";
            }

            // Mostrar Datos
            txaReporte.append(numera + "\t" + cod + "\t" + nom + "\t" + exp + "\t" + veh + "\t" + aux.calificacion + "\n");
            aux = aux.sig;

        }

    }

    Nodo Buscar(Nodo inicio, String cod) {

        Nodo pos = inicio;
        //Recorriendo la lista para encontrar la informacion
        while (pos != null && !cod.equalsIgnoreCase(pos.id)) {
            pos = pos.sig;
        }
        return pos;
    }

    Nodo InsertarInicio(Nodo inicio, String id, String nombre, String exp, String veh, String cal) {
        Nodo nuevo = new Nodo(id, nombre, exp, veh, cal);
        nuevo.sig = inicio;
        return nuevo;
    }

    void Eliminar(Nodo actual) {

        //Creando un puntero al nodo anterior del actual
        Nodo anterior = ini;
        //1. Buscando el nodo anterior
        while (anterior.sig != actual && anterior.sig != null) {
            anterior = anterior.sig;
        }

        if (actual != null) {
            if (anterior == actual) {
                //Borrar el primer nodo
                ini = actual.sig;
            } else {
                anterior.sig = actual.sig;
            }
        }

    }

